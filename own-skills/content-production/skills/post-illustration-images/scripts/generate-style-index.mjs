#!/usr/bin/env node

import { mkdirSync, readFileSync, renameSync, rmSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const skillRoot = resolve(scriptDir, "..");

function markdownCell(value) {
  return String(value).replaceAll("|", "\\|").replaceAll("\n", " ");
}

function code(value) {
  return `\`${String(value).replaceAll("`", "\\`")}\``;
}

function assertRegistry(registry) {
  if (registry?.schemaVersion !== 1) {
    throw new Error("Style registry schemaVersion must be 1");
  }
  if (!Array.isArray(registry.platforms) || registry.platforms.length === 0) {
    throw new Error("Style registry must define platforms");
  }
  if (!Array.isArray(registry.styles) || registry.styles.length === 0) {
    throw new Error("Style registry must define styles");
  }

  const platformIds = new Set();
  for (const platform of registry.platforms) {
    if (!platform?.id || platformIds.has(platform.id)) {
      throw new Error(`Duplicate or missing platform id: ${platform?.id ?? "<missing>"}`);
    }
    platformIds.add(platform.id);
  }

  const styleIds = new Set();
  for (const style of registry.styles) {
    if (!style?.id || styleIds.has(style.id)) {
      throw new Error(`Duplicate or missing style id: ${style?.id ?? "<missing>"}`);
    }
    if (!platformIds.has(style.platform)) {
      throw new Error(`Style ${style.id} uses unknown platform ${style.platform}`);
    }
    for (const key of ["styleFile", "specFile", "styleReference", "defaultUse"]) {
      if (typeof style[key] !== "string" || style[key].length === 0) {
        throw new Error(`Style ${style.id} is missing ${key}`);
      }
    }
    if (!Array.isArray(style.aliases)) {
      throw new Error(`Style ${style.id} aliases must be an array`);
    }
    if (style.provenanceFile !== null && (typeof style.provenanceFile !== "string" || style.provenanceFile.length === 0)) {
      throw new Error(`Style ${style.id} provenanceFile must be null or a non-empty string`);
    }
    styleIds.add(style.id);
  }

  for (const platform of registry.platforms) {
    if (!styleIds.has(platform.defaultStyleId)) {
      throw new Error(`Platform ${platform.id} has unknown defaultStyleId ${platform.defaultStyleId}`);
    }
    const defaultStyle = registry.styles.find((style) => style.id === platform.defaultStyleId);
    if (defaultStyle.platform !== platform.id) {
      throw new Error(`Platform ${platform.id} default style belongs to ${defaultStyle.platform}`);
    }
  }
}

export function renderStyleIndex(registry) {
  assertRegistry(registry);

  const platformById = new Map(registry.platforms.map((platform) => [platform.id, platform]));
  const lines = [
    "# Style Index",
    "",
    "<!-- Generated by scripts/generate-style-index.mjs from references/style-registry.json. Do not edit by hand. -->",
    "",
    "This file routes platform requests to suite-level Style Specs.",
    "",
    "Important:",
    "",
    "- `references/style-registry.json` is the machine-readable source of truth for registered styles and routing metadata.",
    "- Style files define visual language and content expression rules. Machine-readable specs define deterministic geometry, QA, colors, safe areas, component slots, and brand defaults.",
    "- Style Spec canvas dimensions are design-coordinate geometry for target ratio and layout. Accepted model output pixels are preserved for delivery; request dimensions come from the verified generation geometry profile.",
    "- Style reference images are QA baselines only. They are never generation inputs and their semantic content must not be copied.",
    "- Brand resolution is: explicit user choice, then `brandPolicy.defaultEnabled`, then compatibility default `true` for an older spec without `brandPolicy`.",
    "- When branding resolves disabled, the brand area is not an active reservation, although every production spec retains valid top-right brand geometry.",
    "",
    "| Style ID | Platform | Style File | Machine Spec | Style Reference | Brand Plugin | Page Badges | Default Use |",
    "|---|---|---|---|---|---|---|---|"
  ];

  for (const style of registry.styles) {
    const platform = platformById.get(style.platform);
    lines.push(
      `| ${code(style.id)} | ${markdownCell(platform.displayName)} | ${code(style.styleFile)} | ${code(style.specFile)} | ${code(style.styleReference)} | spec default, user-overridable | disabled | ${markdownCell(style.defaultUse)} |`
    );
  }

  lines.push("", "## Platform Routing", "");
  for (const platform of registry.platforms) {
    lines.push(`Use ${platform.displayName} when the user says:`, "");
    for (const phrase of platform.routingPhrases ?? []) {
      lines.push(`- ${phrase}`);
    }
    lines.push("");
  }

  lines.push(
    "If the platform is not stated and cannot be inferred from the source content, ask which platform to use.",
    "",
    "## Style Selection",
    "",
    "1. If the user names a registered style ID, use it.",
    "2. If the user names an alias, map it to the owning style ID.",
    "3. If no style is specified, use the platform's registered default style.",
    "4. For multiple platform styles, select by content expression: process, concept, comparison, or caution/checklist.",
    "",
    "## Natural Language Style Aliases",
    "",
    "| User phrase | Style ID |",
    "|---|---|"
  );

  const stylesWithAliases = registry.styles.filter((style) => style.aliases.length > 0);
  if (stylesWithAliases.length === 0) {
    lines.push("| _None registered_ | | ");
  } else {
    for (const style of stylesWithAliases) {
      lines.push(`| ${markdownCell(style.aliases.join(" / "))} | ${code(style.id)} |`);
    }
  }

  lines.push(
    "",
    "Do not preselect image count, aspect ratio, palette, safe area, brand geometry, page badges, or other fixed slots before selecting a style.",
    "",
    "Use each registered spec as the source of truth for scripts and QA. Use its Style Reference only for visual-system QA and failure review; ignore reference semantics and watermark state.",
    ""
  );

  return lines.join("\n");
}

export function generateStyleIndex({ registryPath, outputPath }) {
  const registry = JSON.parse(readFileSync(registryPath, "utf8"));
  const output = renderStyleIndex(registry);
  mkdirSync(dirname(outputPath), { recursive: true });
  const temporaryPath = `${outputPath}.tmp-${process.pid}`;
  try {
    writeFileSync(temporaryPath, output);
    renameSync(temporaryPath, outputPath);
  } finally {
    rmSync(temporaryPath, { force: true });
  }
  return output;
}

function parseArgs(argv) {
  const args = { check: false };
  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];
    if (argument === "--check") {
      args.check = true;
      continue;
    }
    if (argument !== "--registry" && argument !== "--output") {
      throw new Error(`Unknown argument: ${argument}`);
    }
    const value = argv[index + 1];
    if (!value || value.startsWith("--")) {
      throw new Error(`Missing value for ${argument}`);
    }
    args[argument.slice(2)] = value;
    index += 1;
  }
  return args;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const registryPath = resolve(args.registry ?? resolve(skillRoot, "references/style-registry.json"));
  const registry = JSON.parse(readFileSync(registryPath, "utf8"));
  const outputPath = resolve(args.output ?? resolve(skillRoot, registry.indexFile ?? "references/style-index.md"));
  const rendered = renderStyleIndex(registry);

  if (args.check) {
    const current = readFileSync(outputPath, "utf8");
    if (current !== rendered) {
      throw new Error(`${outputPath} is out of date; regenerate it from ${registryPath}`);
    }
    console.log(`${outputPath} is up to date`);
    return;
  }

  generateStyleIndex({ registryPath, outputPath });
  console.log(`Generated ${outputPath}`);
}

const isMain = process.argv[1] && pathToFileURL(resolve(process.argv[1])).href === import.meta.url;
if (isMain) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    process.exit(1);
  }
}
